    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String jc1=jComboBox1.getSelectedItem().toString();
        String gender = "";
         if(jMale.isSelected())
            {
                gender = "Male";
                
            }
            if(jFemale.isSelected())
            {
                gender = "Female";
                
            }
           
        if(jUname.getText().isBlank()||jPass.getText().isBlank() ||jName.getText().isBlank() || gender.equals("") || jAge.getText().isBlank()||  
                jAddr.getText().isBlank()|| jCity.getText().isBlank()|| jState.getText().isBlank()|| jCountry.getText().isBlank()||jZip.getText().isEmpty()|| jPhone.getText().isBlank()|| jEmail.getText().isBlank()||jc1.equals("Select UserType"))
        {
            JOptionPane.showMessageDialog(this,
                    "Please Enter All Fields",
                    "Try Again",
                    JOptionPane.ERROR_MESSAGE);
        }
        else{
           
            if(!Pattern.matches("^[A-Za-z\\s]{1,}[\\.]{0,1}[A-Za-z\\s]{0,}$",jName.getText()))
            {
                JOptionPane.showMessageDialog(this,"Please Enter Valid Name  !!!");
            }
            else if(!Pattern.matches("^[0-9].*$",jAge.getText()))
            {
                JOptionPane.showMessageDialog(this,"Please Enter Valid Age !!!");
            }
            else if(!Pattern.matches("^[0-9].*$", jZip.getText()))
            {
                JOptionPane.showMessageDialog(this,"Please Enter Valid Zipcode !!!");
                
            }
            else if (!Pattern.matches("\\d{10}",jPhone.getText()))
            {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Number  !!!");
                
            }
            else if(!Pattern.matches("^[\\w\\-]([\\.\\w])+[\\w]+@([\\w\\-]+\\.)+[a-z]{2,4}$",jEmail.getText()))
            {
                JOptionPane.showMessageDialog(this,"Please Enter Valid Email !!!");
            }
            else{
                
                 String admid = jUname.getText();
        String Pass = jPass.getText();
        String Name  = jName.getText();
      
        long Age = Long.parseLong(jAge.getText());
        String Addr = jAddr.getText();
        String City = jCity.getText();
        String State = jState.getText();
        String Country = jCountry.getText();
        String Zip = jZip.getText();
        long Phone =Long.parseLong(jPhone.getText());
        String Email = jEmail.getText();
        
       String jc =jComboBox1.getSelectedItem().toString();
        
        
                try {
                 con = Connect.ConnectDB();
                 String qry ="Insert into admin (adminid,pass,name,gender,Age,addr,city,state,con,zip,num,email,utype) value(?,?,?,?,?,?,?,?,?,?,?,?,?)";
                pst =con.prepareStatement(qry);
                pst.setString(1, admid);
                pst.setString(2, Pass);
                pst.setString(3, Name);
                pst.setString(4, gender);
                pst.setLong(5, Age);
                pst.setString(6,Addr );
                pst.setString(7, City);
                pst.setString(8, State);
                pst.setString(9, Country);
                pst.setString(10, Zip);
                pst.setLong(11, Phone);
                pst.setString(12, Email);
                 pst.setString(13, jc);               
                              
                pst.execute();
                
                 JOptionPane.showMessageDialog(null, "Saved" );
                
              
                } catch (SQLException ex) {
                    Logger.getLogger(LogisticAdmin.class.getName()).log(Level.SEVERE, null, ex);
                }
            
            }
        
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed


